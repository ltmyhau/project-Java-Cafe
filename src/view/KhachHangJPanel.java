    public void layDuLieuTable() {
        List<KhachHang> listKhachHang = new KhachHangDao().getList();
        model = (DefaultTableModel) jtbKhachHang.getModel();
        model.setRowCount(0);
        for (KhachHang i : listKhachHang) {
            model.addRow(new Object[]{
                i.getMaKhachHang(),
                i.getHoTen(),
                i.getNgaySinh(),
                i.getGioiTinh(),
                i.getDienThoai()
            });
        }
    }

    public static void hienThiTable() {
        DefaultTableModel model = new DefaultTableModel();
        List<KhachHang> listKhachHang = new KhachHangDao().getList();
        model = (DefaultTableModel) jtbKhachHang.getModel();
        model.setRowCount(0);
        for (KhachHang i : listKhachHang) {
            model.addRow(new Object[]{
                i.getMaKhachHang(),
                i.getHoTen(),
                i.getNgaySinh(),
                i.getGioiTinh(),
                i.getDienThoai()
            });
        }
    }

    public void sort() {
        TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<DefaultTableModel>(model);
        jtbKhachHang.setRowSorter(rowSorter);
    }

    public void search() {
        model = (DefaultTableModel) jtbKhachHang.getModel();
        TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<>(model);
        jtbKhachHang.setRowSorter(rowSorter);
        rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + jtfTimKiem.getText()));
    }

    public void addPlaceholderStyle(JTextField jTextField) {
        Font font = jTextField.getFont();
        font = font.deriveFont(Font.ITALIC);
        jTextField.setFont(font);
        jTextField.setForeground(Color.GRAY);
    }

    public void removePlaceholderStyle(JTextField jTextField) {
        Font font = jTextField.getFont();
        font = font.deriveFont(Font.PLAIN);
        jTextField.setFont(font);
        jTextField.setForeground(Color.BLACK);
    }

    public void hienThongTin(KhachHangJFrame khachHangJFrame, int row) {
        List<KhachHang> listKhachHang = new KhachHangDao().getList();
        KhachHang khachHang = listKhachHang.get(row);

        khachHangJFrame.setTitle("Thông tin nhân viên");
        khachHangJFrame.setVisible(true);
        khachHangJFrame.setResizable(false);
        khachHangJFrame.setLocationRelativeTo(null);

        khachHangJFrame.select(khachHang);
    }

    public void openFile(String file) {
        try {
            File path = new File(file);
            Desktop.getDesktop().open(path);
        } catch (IOException ioe) {
            System.out.println(ioe);
        }
    }

