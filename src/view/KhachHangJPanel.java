    private void jbnXuatBaoCaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnXuatBaoCaoActionPerformed
        try {
            JFileChooser jFileChooser = new JFileChooser();
            jFileChooser.showSaveDialog(this);
            File saveFile = jFileChooser.getSelectedFile();

            if (saveFile != null) {
                saveFile = new File(saveFile.toString() + ".xlsx");
                XSSFWorkbook wb = new XSSFWorkbook();
                XSSFSheet sheet = wb.createSheet("Khách Hàng");

                XSSFRow row = null;
                Cell cell = null;

                row = sheet.createRow((short) 1);
                row.setHeight((short) 500);
                cell = row.createCell(0, CellType.STRING);
                cell.setCellValue("DANH SÁCH KHÁCH HÀNG");

                row = sheet.createRow((short) 3);
                row.setHeight((short) 500);
                cell = row.createCell(0, CellType.STRING);
                cell.setCellValue("Mã khách hàng");
                cell = row.createCell(1, CellType.STRING);
                cell.setCellValue("Họ và tên");
                cell = row.createCell(2, CellType.STRING);
                cell.setCellValue("Ngày sinh");
                cell = row.createCell(3, CellType.STRING);
                cell.setCellValue("Giới tính");
                cell = row.createCell(4, CellType.STRING);
                cell.setCellValue("Điện thoại");
                cell = row.createCell(5, CellType.STRING);
                cell.setCellValue("Địa chỉ");

                List<KhachHang> listKhachHang = new KhachHangDao().getList();

                for (int i = 0; i < listKhachHang.size(); i++) {
                    KhachHang khachHang = listKhachHang.get(i);
                    row = sheet.createRow((short) 4 + i);
                    row.setHeight((short) 400);
                    row.createCell(0).setCellValue(khachHang.getMaKhachHang());
                    row.createCell(1).setCellValue(khachHang.getHoTen());
                    row.createCell(2).setCellValue(khachHang.getNgaySinh().toString());
                    row.createCell(3).setCellValue(khachHang.getGioiTinh());
                    row.createCell(4).setCellValue(khachHang.getDienThoai());
                    row.createCell(5).setCellValue(khachHang.getDiaChi());
                }

                FileOutputStream out = new FileOutputStream(new File(saveFile.toString()));
                wb.write(out);
                wb.close();
                out.close();
                openFile(saveFile.toString());
            } else {
                JOptionPane.showMessageDialog(null, "Không thể tạo file");
            }
        } catch (FileNotFoundException e) {
            System.out.println(e);
        } catch (IOException io) {
            System.out.println(io);
        }
    }//GEN-LAST:event_jbnXuatBaoCaoActionPerformed


